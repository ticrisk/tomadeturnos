                        <h5>¿Qué es un empaquetador de supermercado?</h5>
                        <p class="text-justify">Un empaquetador es aquel jóven de enseñanza superior ya sea centro de formación técnica, instituto profesional o universidad, persona con discapacidad, o por quienes hayan cumplido la edad legal para pensionarse o se encuentren pensionados y que se desempeñan dentro del supermercado con la misión de guardar los productos que compra el cliente en bolsas plásticas, bolsas ecológicas o en cajas para su posterior traslado. A cambio, el empaquetador recibe una propina voluntaria, que debería ser acorde al trabajo realizado. </p>
                        <p class="text-justify">Es por eso, que estamos en una epoca del despertar de los empaques de supermercados, donde entendemos nuestros derechos y obligaciones, donde identificamos a las empresas de propineros que abusan de los empaquetadores universitarios, donde nosotros como empaques podemos organizarnos, gestionar y desempeñar un correcto labor para llevar a cabo nuestra misión, siempre y cuando contemos con el permiso del supermercado.</p>
                        <blockquote class="blockquote alert-info">
                            <p><i>"Ninguno de nosotros es tan bueno como todos nosotros juntos".</i> <small class="pull-right">Ray Kroc</small></p>
                        </blockquote>

                        <h5>Organizaciones de Empaquetadores Universitarios</h5>
                        <p class="text-justify">Las organizaciones de empaquetadores independientes son las que se gestionan por ellos mismos, llevando un correcto control y orden dentro del supermercado, realizan sus propias reglas y son liderados por una o varias personas que buscan el bienestar de los empaquetadores y por sobre todo de la organización.</p>
                        <p class="text-justify">Si perteneces a una empresa de empaques que abusa de los propineros, ¡rebélate!. El momento es ahora, hay que luchar por nuestro derechos. No esperes actuar cuando las injusticias te afecten. Reúnanse y cuando sean más de la mitad que deseen detener los abusos, presenten un proyecto al supermercado demostrando las injusticias que se cometen y los beneficios que ustedes entregarán, existe una gran posibilidad en que ellos depositen su confianza en ustedes y logren ser una organización independiente de empaques.</p>
                        <blockquote class="blockquote alert-info">
                            <p><i>"Recuerda que de la conducta de cada uno depende el destino de todos". </i> <small class="pull-right">Alejandro Magno</small></p>
                        </blockquote>

                        <h5>¿Por qué es necesario unificar a los empaquetadores?.</h5>
                        <p class="text-justify">Uno de los objetivos primordiales de Proyecto Nero es unificar a los empaquetadores y lo estamos logrando gracias a las alianzas estratégicas que tienen la misma causa social, que es ayudar y guiar a los propineros de supermercados.</p>
                        <p class="text-justify">Existen dos tipos de unificación en el rubro de los propineros, la primera a nivel local, es reunir todo el apoyo posible dentro de tu organización para librarse de las empresas de empaquetadores y se logra cuando la mayoría toma conciencia de las injusticias que se cometen, de los cobros excesivos y sobre todo cuando se arman de valor para luchar por sus derechos.</p>
                        <p class="text-justify">La segunda es a nivel global, cuando todos los empaquetadores de Chile hagan sentir su voz y los supermercados no hagan oidos sordos y ayuden a los propineros a independizarse de las empresas de empaques, brindando todo el apoyo para continuar con el labor de empacar.</p>
                        <p class="text-justify">Sin duda, hay supermercados que han apoyado a las organizaciones independientes. Logrando una armonía y un buen ambiente de trabajo en el local, ya que las cajeras y los empaquetadores son la cara visible en la atención del cliente.</p>
                        <blockquote class="blockquote alert-info">
                            <p><i>"Los hombres son hermanos y juntos deben trabajar". </i>  <small class="pull-right">Los Prisioneros</small></p>
